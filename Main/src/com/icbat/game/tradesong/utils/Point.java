package com.icbat.game.tradesong.utils;

/**
 * Immutable representation of a 2D point.
 * */
public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
